function t(key) {
  const pack = I18N[currentLocale] || I18N.en;
  return pack[key] ?? I18N.en[key] ?? key;
}

function ti(key, vars) {
  let s = t(key);
  if (vars) {
    Object.keys(vars).forEach((k) => {
      s = s.replace(new RegExp('\\{' + k + '\\}', 'g'), vars[k]);
    });
  }
  return s;
}

function localeName(code) {
  const loc = LOCALES.find((l) => l.code === code);
  if (!loc) return 'English';
  return loc.label;
}

function greetingKey() {
  const h = new Date().getHours();
  if (h < 12) return 'goodMorning';
  if (h < 17) return 'goodAfternoon';
  return 'goodEvening';
}

function itemsCountLabel(count) {
  return count === 1 ? t('itemsCountOne') : ti('itemsCountMany', { count: String(count) });
}

function clipsInOrderLabel(count) {
  return count === 1 ? t('clipsInOrderOne') : ti('clipsInOrderMany', { count: String(count) });
}

function playlistsReadyLabel(count) {
  return count === 1 ? t('playlistsReadyOne') : ti('playlistsReadyMany', { count: String(count) });
}

