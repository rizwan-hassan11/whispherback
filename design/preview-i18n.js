function applyLocale() {
  document.documentElement.lang = currentLocale;
  document.documentElement.dir = (currentLocale === 'ar' || currentLocale === 'ur') ? 'rtl' : 'ltr';
  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.dataset.i18n;
    if (key) el.textContent = t(key);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
    const key = el.dataset.i18nPlaceholder;
    if (key) el.placeholder = t(key);
  });
  document.querySelectorAll('[data-i18n-title]').forEach((el) => {
    const key = el.dataset.i18nTitle;
    if (key) el.title = t(key);
  });
  document.querySelectorAll('[data-i18n-aria]').forEach((el) => {
    const key = el.dataset.i18nAria;
    if (key) el.setAttribute('aria-label', t(key));
  });
  const langValue = document.getElementById('settingsLangValue');
  if (langValue) langValue.textContent = localeName(currentLocale);
  updateDynamicTexts();
}

function openLanguagePicker() {
  const list = document.getElementById('languageList');
  const modal = document.getElementById('languageModal');
  if (!list || !modal) return;
  list.innerHTML = LOCALES.map((l) => `
    <button type="button" class="lang-option ${l.code === currentLocale ? 'on' : ''}" onclick="setLocale('${l.code}')">
      <span class="lang-option-text">
        <strong>${l.label}</strong>
        <small>${l.native}</small>
      </span>
      ${l.code === currentLocale ? '<i data-lucide="check"></i>' : ''}
    </button>`).join('');
  modal.classList.add('show');
  if (typeof refreshIcons === 'function') refreshIcons(modal);
}

function closeLanguagePicker() {
  document.getElementById('languageModal')?.classList.remove('show');
}

function setLocale(code) {
  currentLocale = code;
  window.currentLocale = code;
  localStorage.setItem('wb-locale', code);
  applyLocale();
  if (typeof buildNavs === 'function') buildNavs();
  closeLanguagePicker();
  if (typeof toast === 'function') toast(t('languageChanged'));
}

if (typeof window !== 'undefined') {
  window.LOCALES = LOCALES;
  window.I18N = I18N;
  window.currentLocale = currentLocale;
  window.t = t;
  window.ti = ti;
  window.applyLocale = applyLocale;
  window.updateDynamicTexts = updateDynamicTexts;
  window.greetingKey = greetingKey;
  window.localeName = localeName;
  window.setLocale = setLocale;
  window.openLanguagePicker = openLanguagePicker;
  window.closeLanguagePicker = closeLanguagePicker;
  window.itemsCountLabel = itemsCountLabel;
  window.clipsInOrderLabel = clipsInOrderLabel;
  window.playlistsReadyLabel = playlistsReadyLabel;
}
